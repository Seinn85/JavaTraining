public class Lesson8{
   public static void main(String[] args){
      //for文を使用して 1から10までの数を順に表示する
      for (int i = 1; i <= 10; i++) {
         System.out.println(i);
      }

      System.out.println();  // Print an empty line for separation

      //for文を使用して 10から1までの数を逆順に表示する
      for (int i = 10; i >= 1; i--) {
         System.out.println(i);
      }
   }
}